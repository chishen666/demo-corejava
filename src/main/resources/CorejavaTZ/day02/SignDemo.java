package com.corejava.day02;

/**
 * 本类用来演示Java特殊符号
 *
 * 作者:JSD1412
 * 时间:2015-1-4 下午4:35:49
 */
public class SignDemo {
	public static void main(String[] args) {
		//换行
		System.out.println("好好学习,\n天天向上.");
	
		//回车
		System.out.println("好好学习,\r天天向上.");
		
		//tab距离
		System.out.println("好好学习,\t天天向上.");
		
		
		//通过转译符来转译,\
		System.out.println("考试成绩在D:\\rank目录下.");
		System.out.println("我的学习资料在C:\\temp目录下.");
		System.out.println("篮球资料在E:\\nba目录下.");
		
		System.out.println("Rose:\"You jump,I jump!\"");
		
		System.out.println("\\\\");
	}
}
